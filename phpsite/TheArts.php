                            <img title="Neha performing Mohiniattam" alt="Neha performing Mohiniattam" src="images/NehaMohiniAttam_03_WM.jpg" style="float:left; margin-right:10px;"/>Mohiniyattam, also spelt Mohiniattam  is a traditional South Indian dance from Kerala, developed by the Tamil nattuvanar (dance master) Vadivelu,one of the Thanjavur Quartet.<br /> 
                            It is a feminine dance form. The term Mohiniattam comes from the words "Mohini" meaning a woman who enchants onlookers and "aattam" meaning graceful and sensuous body movements.<br />  
                            The word "Mohiniattam" literally means "dance of the enchantress". This dance which has influences and elements from two South Indian dance forms, the Bharatanatyam and Kathakali, was formulated in the court of King Swati Tirunal by Vadivelu.<br />  
                            <img title="Neha depicting wonder in Mohiniattam"  alt="Neha depicting wonder in Mohiniattam" src="images/NehaMohiniAttam_04_WM.jpg" style="float:right; margin-left:10px;"/>Here the movements of the dancer's body could be compared to the gently undulating palm fronds filling up the Kerala skyline.<br />  
                            There is a lot of heaving of the chest and swaying movements like the coconut palms. The gait and the stepping are reminiscent of a boat bobbing up and down in the gently soaring back waters of the kerala landscape, the land of Mohiniattam.<br />                         
